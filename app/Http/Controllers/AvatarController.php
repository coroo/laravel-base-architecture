     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = Auth::user();
        $avatarDetail = $user->addMediaFromRequest('avatar')->toMediaCollection('avatars');
        if(isset($avatarDetail->order_column)){
            $updateDetail = [
                'image_path'    => $avatarDetail->order_column.'/'.$avatarDetail->file_name
            ];
            $this->userProfileService->update(collect($updateDetail), $user->id);
        }
        return redirect()->back();
    }

